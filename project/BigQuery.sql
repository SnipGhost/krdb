SELECT
    T.`Время_занятия`,
    GROUP_CONCAT(IF(S.`День_недели` = '1. ПН', P.`Наименование`, NULL)) AS 'ПН',
    GROUP_CONCAT(IF(S.`День_недели` = '2. ВТ', P.`Наименование`, NULL)) AS 'ВТ',
    GROUP_CONCAT(IF(S.`День_недели` = '3. СР', P.`Наименование`, NULL)) AS 'СР',
    GROUP_CONCAT(IF(S.`День_недели` = '4. ЧТ', P.`Наименование`, NULL)) AS 'ЧТ',
    GROUP_CONCAT(IF(S.`День_недели` = '5. ПТ', P.`Наименование`, NULL)) AS 'ПТ',
    GROUP_CONCAT(IF(S.`День_недели` = '6. СБ', P.`Наименование`, NULL)) AS 'СБ'

FROM `Занятия` AS S
    INNER JOIN Предметы     AS P ON S.`Предмет_ID` = P.`Предмет_ID`
    INNER JOIN Время        AS T ON S.`Время_ID` = T.`Время_ID`
    INNER JOIN Пользователи AS U ON S.`Класс_ID` = U.`Класс_ID`
    INNER JOIN Подгруппы    AS G ON S.`Подгруппа_ID` = G.`Подгруппа_ID`

WHERE ((U.`ФИО` = 'Кучеренко М.А.') AND 
    ((S.`Подгруппа_ID`) = U.`Подгруппа_ID` OR (S.`Подгруппа_ID`) = 8 OR (S.`Подгруппа_ID`) IS NULL))

GROUP BY T.`Время_занятия`


