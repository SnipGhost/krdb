    GROUP_CONCAT(IF(z.`День_недели` = '2. ВТ', p.`Наименование`, NULL)) AS 'ВТ',
    GROUP_CONCAT(IF(z.`День_недели` = '3. СР', p.`Наименование`, NULL)) AS 'СР',
    GROUP_CONCAT(IF(z.`День_недели` = '4. ЧТ', p.`Наименование`, NULL)) AS 'ЧТ',
    GROUP_CONCAT(IF(z.`День_недели` = '5. ПТ', p.`Наименование`, NULL)) AS 'ПТ',
    GROUP_CONCAT(IF(z.`День_недели` = '6. СЮ', p.`Наименование`, NULL)) AS 'СБ'
FROM `Занятия` AS z
LEFT JOIN `Предметы` AS p ON p.`Предмет_ID` = z.`Предмет_ID`
LEFT JOIN `Время`    AS t ON t.`Время_ID` = z.`Время_ID`
WHERE z.`Класс_ID` = '1' AND (z.`Подгруппа_ID` = '1' OR z.`Подгруппа_ID` IS NULL)
GROUP BY t.`Время_занятия`